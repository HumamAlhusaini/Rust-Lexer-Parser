open Token

let pp_token = function
  | ADD -> "ADD"
  | MULT -> "MULT"
  | INT i -> Printf.sprintf "INT(%d)" i
  | EOF -> "EOF"
  | FUNC -> "FUNC"
  | STRUCT -> "STRUCT"
  | ID s -> Printf.sprintf "ID(%s)" s
  | LBRAC -> "LBRAC"
  | RBRAC -> "RBRAC"
  | RPAREN -> "RPAREN"
  | LPAREN -> "LPAREN"
  | PRINTF -> "PRINTF"
  | STRING s -> Printf.sprintf "STRING(%s)" s
  | COMMA -> "COMMA"
  | SEMICOLON -> "SEMICOLON"
  | COLON -> "COLON"
  | AMPERSAND -> "AMPERSAND"
  | STR -> "TYPE STR"
  | STATIC -> "STATIC"
  | EQUALS -> "EQUALS"
  | ISEQUAL -> "ISEQUALS"
  | PERIOD -> "PERIOD"
  | SUB -> "SUB"
  | DIV -> "DIV"
  | MOD -> "MOD"
  | LT -> "LT"
  | GT -> "GT"
  | LTE -> "LTE"
  | GTE -> "GTE"
  | NEQ -> "NEQ"
  | AND -> "AND"
  | OR -> "OR"
  | NOT -> "NOT"
  | BAND -> "BAND"
  | BOR -> "BOR"
  | XOR -> "XOR"
  | BNOT -> "BNOT"
  | LSHIFT -> "LSHIFT"
  | RSHIFT -> "RSHIFT"
  | PLUSEQ -> "PLUSEQ"
  | MINUSEQ -> "MINUSEQ"
  | MULTEQ -> "MULTEQ"
  | DIVEQ -> "DIVEQ"
  | MODEQ -> "MODEQ"
  | BANDEQ -> "BANDEQ"
  | BOREQ -> "BOREQ"
  | XOREQ -> "XOREQ"
  | LSHIFTEQ -> "LSHIFTEQ"
  | RSHIFTEQ -> "RSHIFTEQ"
  | TRUE -> "TRUE"
  | FALSE -> "FALSE"
  | LET -> "LET"
  | I32 -> "I32"
  | U32 -> "U32"
  | I64 -> "I64"
  | U64 -> "U64"
  | F32 -> "F32"
  | F64 -> "F64"
  | BOOL -> "BOOL"
  | CHAR c -> Printf.sprintf "CHAR(%c)" c
  | MUT -> "MUT"
  | IF -> "IF"
  | ELSE -> "ELSE"
  | WHILE -> "WHILE"
  | FOR -> "FOR"
  | IN -> "IN"
  | BREAK -> "BREAK"
  | CONTINUE -> "CONTINUE"
  | RETURN -> "RETURN"
  | MATCH -> "MATCH"
  | ARM -> "ARM"
  | FAT_ARROW -> "FAT_ARROW"
  | UNDERSCORE -> "UNDERSCORE"
  | DOUBLE_COLON -> "DOUBLE_COLON"
  | SINGLE_QUOTE -> "SINGLE_QUOTE"
  | HASH -> "HASH"
  | AT -> "AT"
  | DOLLAR -> "DOLLAR"
  | QUESTION -> "QUESTION"
  | RAW_STRING s -> Printf.sprintf "RAW_STRING(%s)" s
  | AS -> "AS"
  | USE -> "USE"
  | CRATE -> "CRATE"
  | PUB -> "PUB"
  | IMPL -> "IMPL"
  | TRAIT -> "TRAIT"
  | TYPE -> "TYPE"
  | ENUM -> "ENUM"
  | CONST -> "CONST"
  | STATIC_KEYWORD -> "STATIC_KEYWORD"
  | UNSAFE -> "UNSAFE"
  | EXTERN -> "EXTERN"
  | ASYNC -> "ASYNC"
  | AWAIT -> "AWAIT"
  | TRY -> "TRY"
  | BOX -> "BOX"
  | ARC -> "ARC"
  | RC -> "RC"
  | VEC -> "VEC"
  | OPTION -> "OPTION"
  | RESULT -> "RESULT"
  | SELF -> "SELF"
  | SELF_KEYWORD -> "SELF_KEYWORD"
  | SUPER -> "SUPER"
  | SUPER_KEYWORD -> "SUPER_KEYWORD"
  | SELF_TYPE -> "SELF_TYPE"
